//Function def using arrow
var add=(num1,num2) =>num1+num2;                          //Add
var welcome=()=>"Welcome to JS";                         //String
var squar=(num) =>num*num;                               //Square
var greeting= (name)=>"Hello"+name;                     //Greeting
var power=(num1,num2)=>Math.pow(num1,num2);             //Power
//Function call 
console.log(add(5,6));
console.log(welcome());
console.log(squar(5));
console.log(greeting (" Raj "));
console.log(power(3,3));